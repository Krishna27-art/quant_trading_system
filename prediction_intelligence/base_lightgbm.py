"""
Base LightGBM Classifier

Binary classifier for a single timeframe (INTRADAY / SWING / LONGTERM).
Produces calibrated win probabilities consumed by generate_live_predictions.py.

Contract with generate_live_predictions.py:
  - Saved to:  models/saved/{timeframe}_lgbm.pkl   (e.g. intraday_lgbm.pkl)
  - Loaded by: load_model(config["model_file"])
  - Called as: model.predict_proba(X)[0]  → [p_loss, p_win]

Training input contract (must match build_features() in generate_live_predictions.py):
  FEATURE_COLS = [
      "rsi_14", "ma20_z", "ma50_z", "atr_pct", "vwap_dist",
      "vol_ratio", "ret_1", "ret_5", "ret_20",
      "vix_level", "market_regime", "usd_inr_chg", "dow_chg",
  ]

Label contract:
  y = 1  if price hit target before stop-loss within the horizon
  y = 0  if price hit stop-loss first, or horizon expired flat
  (generated by QuantResearchOS/services/label_engine/triple_barrier.py)

Usage:
  model = BaseLightGBM(timeframe="INTRADAY")
  model.train(X_train, y_train, X_val, y_val)
  model.save()                          # → models/saved/intraday_lgbm.pkl
  model.load()                          # reload for inference
  proba = model.predict_proba(X)        # shape (n, 2)
"""

from __future__ import annotations

import os
import pickle
from pathlib import Path
from typing import Optional

import numpy as np
import pandas as pd

try:
    import lightgbm as lgb
    LIGHTGBM_AVAILABLE = True
except ImportError:
    LIGHTGBM_AVAILABLE = False

from utils.logger import get_logger

logger = get_logger("base_lightgbm")

# Must match FEATURE_COLS in scripts/generate_live_predictions.py exactly.
FEATURE_COLS: list[str] = [
    "rsi_14", "ma20_z", "ma50_z", "atr_pct", "vwap_dist",
    "vol_ratio", "ret_1", "ret_5", "ret_20",
    "vix_level", "market_regime", "usd_inr_chg", "dow_chg",
]

# Hyperparameters per timeframe.
# Intraday: shallower trees, more regularization — high noise, short horizon.
# Swing: balanced.
# Longterm: deeper, more estimators — fundamental signal survives longer.
_PARAMS: dict[str, dict] = {
    "INTRADAY": {
        "objective": "binary",
        "metric": "binary_logloss",
        "n_estimators": 500,
        "learning_rate": 0.03,
        "num_leaves": 15,
        "feature_fraction": 0.6,
        "bagging_fraction": 0.7,
        "bagging_freq": 5,
        "min_child_samples": 50,
        "lambda_l1": 2.0,
        "lambda_l2": 1.0,
        "verbose": -1,
        "n_jobs": -1,
        "random_state": 42,
    },
    "SWING": {
        "objective": "binary",
        "metric": "binary_logloss",
        "n_estimators": 500,
        "learning_rate": 0.03,
        "num_leaves": 31,
        "feature_fraction": 0.7,
        "bagging_fraction": 0.8,
        "bagging_freq": 5,
        "min_child_samples": 40,
        "lambda_l1": 1.5,
        "lambda_l2": 0.5,
        "verbose": -1,
        "n_jobs": -1,
        "random_state": 42,
    },
    "LONGTERM": {
        "objective": "binary",
        "metric": "binary_logloss",
        "n_estimators": 600,
        "learning_rate": 0.02,
        "num_leaves": 31,
        "feature_fraction": 0.7,
        "bagging_fraction": 0.8,
        "bagging_freq": 5,
        "min_child_samples": 30,
        "lambda_l1": 1.0,
        "lambda_l2": 0.5,
        "verbose": -1,
        "n_jobs": -1,
        "random_state": 42,
    },
}

_DEFAULT_MODEL_DIR = Path(os.getenv("MODEL_DIR", "models/saved"))


class BaseLightGBM:
    """
    LightGBM binary classifier for one trading timeframe.

    Wraps lgb.LGBMClassifier with:
      - early stopping on a held-out validation set
      - feature importance logging
      - save / load to the path that generate_live_predictions.py expects
      - predict_proba returning shape (n_samples, 2) — compatible with sklearn API
    """

    def __init__(
        self,
        timeframe: str = "SWING",
        model_dir: Path | str | None = None,
        early_stopping_rounds: int = 40,
    ):
        if timeframe not in _PARAMS:
            raise ValueError(f"timeframe must be one of {list(_PARAMS)}; got '{timeframe}'")

        self.timeframe = timeframe.upper()
        self.model_dir = Path(model_dir) if model_dir else _DEFAULT_MODEL_DIR
        self.early_stopping_rounds = early_stopping_rounds
        self.feature_names: list[str] = FEATURE_COLS
        self.model: Optional["lgb.LGBMClassifier"] = None
        self._train_metrics: dict = {}

        if not LIGHTGBM_AVAILABLE:
            raise ImportError("lightgbm is not installed. Run: pip install lightgbm")

    # ------------------------------------------------------------------
    # Paths
    # ------------------------------------------------------------------

    @property
    def save_path(self) -> Path:
        """Path that generate_live_predictions.py will load from."""
        return self.model_dir / f"{self.timeframe.lower()}_lgbm.pkl"

    # ------------------------------------------------------------------
    # Training
    # ------------------------------------------------------------------

    def train(
        self,
        X_train: pd.DataFrame,
        y_train: pd.Series,
        X_val: pd.DataFrame,
        y_val: pd.Series,
    ) -> dict:
        """
        Fit on training data; use validation set for early stopping.

        Args:
            X_train: Feature DataFrame. Must contain all FEATURE_COLS.
            y_train: Binary labels (1 = win, 0 = loss). No NaN allowed.
            X_val:   Held-out validation features (same columns).
            y_val:   Held-out validation labels.

        Returns:
            dict with val_logloss, val_auc, feature_importance.

        Raises:
            ValueError if columns are missing or labels contain NaN.
        """
        self._validate_inputs(X_train, y_train, "train")
        self._validate_inputs(X_val, y_val, "val")

        X_tr = X_train[self.feature_names].values.astype(np.float32)
        X_vl = X_val[self.feature_names].values.astype(np.float32)
        y_tr = y_train.values.astype(np.int32)
        y_vl = y_val.values.astype(np.int32)

        pos_rate = y_tr.mean()
        logger.info(
            f"[{self.timeframe}] Training: n={len(y_tr)} pos_rate={pos_rate:.3f} "
            f"val_n={len(y_vl)} val_pos_rate={y_vl.mean():.3f}"
        )

        # Class weight for imbalanced labels (common in triple-barrier)
        scale_pos_weight = (1 - pos_rate) / pos_rate if pos_rate > 0 else 1.0

        params = dict(_PARAMS[self.timeframe])
        params["scale_pos_weight"] = round(scale_pos_weight, 3)

        self.model = lgb.LGBMClassifier(**params)
        self.model.fit(
            X_tr, y_tr,
            eval_set=[(X_vl, y_vl)],
            eval_metric="binary_logloss",
            callbacks=[
                lgb.early_stopping(self.early_stopping_rounds, verbose=False),
                lgb.log_evaluation(period=50),
            ],
        )

        # Validation metrics
        val_proba = self.model.predict_proba(X_vl)[:, 1]
        val_logloss = _log_loss(y_vl, val_proba)
        val_auc = _roc_auc(y_vl, val_proba)

        # Feature importance
        importance = dict(zip(
            self.feature_names,
            self.model.feature_importances_.tolist(),
        ))
        top = sorted(importance.items(), key=lambda x: x[1], reverse=True)[:5]

        self._train_metrics = {
            "timeframe": self.timeframe,
            "n_train": int(len(y_tr)),
            "n_val": int(len(y_vl)),
            "pos_rate_train": round(float(pos_rate), 4),
            "val_logloss": round(float(val_logloss), 5),
            "val_auc": round(float(val_auc), 4),
            "best_iteration": int(self.model.best_iteration_),
            "top_features": top,
        }

        logger.info(
            f"[{self.timeframe}] Done. "
            f"best_iter={self.model.best_iteration_} "
            f"val_logloss={val_logloss:.5f} val_auc={val_auc:.4f}"
        )
        logger.info(f"[{self.timeframe}] Top features: {top}")

        return self._train_metrics

    # ------------------------------------------------------------------
    # Inference
    # ------------------------------------------------------------------

    def predict_proba(self, X: pd.DataFrame) -> np.ndarray:
        """
        Return class probabilities, shape (n_samples, 2).
        Column 0 = P(loss), Column 1 = P(win).

        Compatible with sklearn predict_proba API and with
        generate_live_predictions.py: model.predict_proba(X)[0][1].
        """
        self._require_trained()
        missing = [c for c in self.feature_names if c not in X.columns]
        if missing:
            raise ValueError(f"Missing columns in X: {missing}")
        arr = X[self.feature_names].values.astype(np.float32)
        return self.model.predict_proba(arr)

    def predict(self, X: pd.DataFrame) -> np.ndarray:
        """Hard predictions (0 or 1). Mostly for evaluation."""
        proba = self.predict_proba(X)
        return (proba[:, 1] >= 0.5).astype(np.int32)

    # ------------------------------------------------------------------
    # Persistence
    # ------------------------------------------------------------------

    def save(self, path: Path | str | None = None) -> Path:
        """
        Serialize model to disk.
        Default path: models/saved/{timeframe.lower()}_lgbm.pkl

        This is the path generate_live_predictions.py loads from.
        """
        self._require_trained()
        target = Path(path) if path else self.save_path
        target.parent.mkdir(parents=True, exist_ok=True)

        payload = {
            "model": self.model,
            "feature_names": self.feature_names,
            "timeframe": self.timeframe,
            "train_metrics": self._train_metrics,
        }
        with open(target, "wb") as f:
            pickle.dump(payload, f, protocol=pickle.HIGHEST_PROTOCOL)

        logger.info(f"[{self.timeframe}] Model saved → {target}")
        return target

    def load(self, path: Path | str | None = None) -> "BaseLightGBM":
        """
        Load a previously saved model.
        Raises FileNotFoundError if artifact is missing.
        """
        target = Path(path) if path else self.save_path
        if not target.exists():
            raise FileNotFoundError(
                f"No model artifact at {target}. "
                f"Run training/train_models.py to generate it."
            )
        with open(target, "rb") as f:
            payload = pickle.load(f)

        self.model = payload["model"]
        self.feature_names = payload["feature_names"]
        self.timeframe = payload["timeframe"]
        self._train_metrics = payload.get("train_metrics", {})

        logger.info(
            f"[{self.timeframe}] Model loaded ← {target} "
            f"(best_iter={self._train_metrics.get('best_iteration')})"
        )
        return self

    # ------------------------------------------------------------------
    # Diagnostics
    # ------------------------------------------------------------------

    def feature_importance(self) -> pd.Series:
        """Return feature importances as a sorted Series."""
        self._require_trained()
        return pd.Series(
            self.model.feature_importances_,
            index=self.feature_names,
        ).sort_values(ascending=False)

    def metrics(self) -> dict:
        """Return training metrics recorded during the last fit()."""
        return dict(self._train_metrics)

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _require_trained(self) -> None:
        if self.model is None:
            raise RuntimeError(
                f"[{self.timeframe}] Model is not trained. "
                "Call train() or load() first."
            )

    def _validate_inputs(
        self, X: pd.DataFrame, y: pd.Series, split_name: str
    ) -> None:
        missing = [c for c in self.feature_names if c not in X.columns]
        if missing:
            raise ValueError(f"{split_name}: missing feature columns: {missing}")
        if y.isna().any():
            raise ValueError(f"{split_name}: y contains NaN — check triple-barrier labeler output.")
        if set(y.unique()) - {0, 1}:
            raise ValueError(f"{split_name}: y must be binary (0/1), found: {set(y.unique())}")
        if len(X) != len(y):
            raise ValueError(f"{split_name}: X ({len(X)}) and y ({len(y)}) length mismatch.")
        if len(X) < 50:
            raise ValueError(f"{split_name}: only {len(X)} samples — need at least 50.")


# ------------------------------------------------------------------
# Pure-Python metric helpers (avoids mandatory sklearn dependency)
# ------------------------------------------------------------------

def _log_loss(y_true: np.ndarray, y_proba: np.ndarray, eps: float = 1e-7) -> float:
    y_proba = np.clip(y_proba, eps, 1 - eps)
    return float(-np.mean(y_true * np.log(y_proba) + (1 - y_true) * np.log(1 - y_proba)))


def _roc_auc(y_true: np.ndarray, y_score: np.ndarray) -> float:
    """Wilcoxon-Mann-Whitney AUC. O(n log n), no sklearn required."""
    pos = y_score[y_true == 1]
    neg = y_score[y_true == 0]
    if len(pos) == 0 or len(neg) == 0:
        return float("nan")
    # Count concordant pairs via sorting
    combined = np.concatenate([pos, neg])
    labels = np.concatenate([np.ones(len(pos)), np.zeros(len(neg))])
    order = np.argsort(combined)
    labels_sorted = labels[order]
    cumsum_neg = np.cumsum(labels_sorted == 0)
    concordant = np.sum(cumsum_neg[labels_sorted == 1])
    return float(concordant / (len(pos) * len(neg)))