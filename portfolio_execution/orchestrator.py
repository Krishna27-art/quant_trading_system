"""
Central Trading Orchestrator

The brain of the quant platform. Wires the entire pipeline:
    Data Feed → State Manager → Alpha Signals → Risk Filter → Portfolio → OMS → EMS

Runs as a ticking loop on configurable intervals (default: 1-minute bars).
Supports 3 modes: BACKTEST, PAPER, LIVE.

Safety features:
- Watchdog timer kills process if main loop hangs
- Global circuit breaker on max drawdown
- Heartbeat monitoring for all components
- Graceful shutdown with position flattening
"""

